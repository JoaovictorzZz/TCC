// Seta qual produto o boca aberta clicou pra ver
const definirInspecao = (id) => localStorage.setItem("inspecao", id);
  
// Função para carregar a porqueira dos produtos
const carregarProdutos = () => {
    const produtosRef = document.getElementById("products");

    // Exibe os produtos disponíveis
    //index
    produtos.forEach((item, index) => {
        const produtosCard = document.createElement("div");
        produtosCard.className = 'product-card';

        const imagem = document.createElement("div");
        imagem.className = 'product-image';
        const img = document.createElement("img");
        img.src = item.imagens[0];
        img.alt = `Produto ${index + 1}`;
        imagem.appendChild(img);

        const detalhes = document.createElement("div");
        detalhes.className = 'product-details';

        const titulo = document.createElement("h3");
        titulo.className = 'product-title';
        titulo.textContent = item.nome;

        const preco = document.createElement("p");
        preco.className = 'product-price';
        preco.textContent = item.preco.includes("R$") ? item.preco : `R$${item.preco}`;

        const link = document.createElement('a');
        link.href = '../templates/detalhe.html';

        const comprarBtn = document.createElement('button');
        comprarBtn.className = 'add-to-cart';
        comprarBtn.textContent = 'Comprar';
        comprarBtn.onclick = () =>
           definirInspecao(item.id);
        link.appendChild(comprarBtn);

        detalhes.appendChild(titulo);
        detalhes.appendChild(preco);
        detalhes.appendChild(link);
        produtosCard.appendChild(imagem);
        produtosCard.appendChild(detalhes);
        produtosRef.appendChild(produtosCard);
    });
}

